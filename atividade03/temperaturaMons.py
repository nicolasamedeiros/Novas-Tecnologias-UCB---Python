T = [-10, -8, 0, 1, 2, 5, -2,-4]

maiorTemperatura = max(T)
print("A maior temperatura é {}".format(maiorTemperatura))

menorTemperatura = min(T)
print("A menor temperatura é {}".format(menorTemperatura))

somaTemperatura = sum(T)
mediaTemperatura = somaTemperatura / len(T)
print("A média de temperatura é {}".format(mediaTemperatura))